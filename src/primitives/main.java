package primitives;

public class main {
    public static void main(String args []){
        byte b = 1;
        short sh = 1;
        int i = 1;
        long l = 1L;
        float f = 1f;
        double d = 1d;
        System.out.println("Byte:" + b);
        System.out.println("Short :" + sh);
        System.out.println("int :" + i);
        System.out.println("Long :" + l);
        System.out.println("f:" + f);
        System.out.println("double:" + d);
        System.out.println("Byte:" + b);
    }
}
